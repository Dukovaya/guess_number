# Импорт функции получения случайных чисел
# из модуля random.
from random import randint

# Получаем случайное число в диапазоне от 1 до 100.
number = randint(1, 100)
print('Угадайте число от 1 до 100')

def main():
    while True:
        # Получаем число от пользователя и сохраняем его в переменную.
        user_input = input('Введите число: ')
        
        # Проверяем, является ли ввод числом.
        if not user_input.isdigit():
            print('Ошибка: это не число. Введите целое число от 1 до 100.')
            continue

        guess= int(user_input)
        
        # Проверяем диапазон числа.
        if guess < 1 or guess > 100:
            print('Ошибка: число должно быть в диапазоне от 1 до 100.')
            continue

        # Если число меньше загаданного...
        if guess < number:
            # ...выводим сообщение.
            print('Ваше число меньше того, что загадано.')
        elif guess > number:
            # ...выводим сообщение.
            print('Ваше число больше того, что загадано.')
        elif guess == number:
            # ...прерываем выполнение программы и...
            break

main()
# ...выводим сообщение.
print('Отличная интуиция! Вы угадали число :)')
